//switch substitue o uso de muitos if
let cor = "preta";
switch (cor) {
    case "vermelho":
        console.log("A cor é vermelho");
        break;
    case "verde":
        console.log("A cor é verde");
        break;
    case "azul":
        console.log("A cor é azul");
        break;
    default:
        console.log("Cor não reconhecida");
}