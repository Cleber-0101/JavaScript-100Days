//funções uteis
let numero1 =  144
let numero2 = 4.5


console.log(Math.round(numero2)) // arredonda o valor para MAIS
console.log(Math.floor(numero1)) // arredonda o valor para MENOS
console.log(Math.ceil(numero1)) // arredonda o valor para CIMA
console.log(Math.max(numero1 , numero2)) // compara dois valores e MOSTRA O MAIOR valor 
console.log(Math.min(numero1, numero2)) // compara dois valores e MOSTRA o MENOS valor
console.log(Math.random()) //Gera numero aleatorio entre 0 e 1 
console.log(Math.sqrt(numero1))
console.log(Math.pow(numero1 , numero2)) // elevado a potencia



