console.log(5 == "5");   // true  -> Compara apenas o valor, ignora o tipo
console.log(5 === "5");  // false -> Compara valor e tipo
console.log(5 != 10);    // true  -> 5 é diferente de 10
console.log(5 !== "5");  // true  -> Valor igual, mas tipo diferente
console.log(10 > 5);     // true  -> 10 é maior que 5
console.log(5 < 10);     // true  -> 5 é menor que 10
console.log(5 >= 5);     // true  -> 5 é maior ou igual a 5
console.log(3 <= 7);     // true  -> 3 é menor ou igual a 7
